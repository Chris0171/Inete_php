      <h2>¿Qué quiere decir originariamente español?</h2>
      <ul>
        <li>
          Para ser considerado originariamente español, no hace falta haber nacido en España. La legislación española
          establece la transmisión de la nacionalidad “ius sanguinis" (por sangre), por tanto, se consideran
          originariamente españoles los nacidos de padre o madre españoles independientemente de que hayan nacido en
          España o en el exterior. En el caso de aquellos nacidos en el extranjero, serán considerados originariamente
          españoles solo si pueden demostrar que el padre o madre/abuela o abuelo (que emigraron) era español cuando
          nació
          el hijo/a, (que no había perdido o renunciado a su nacionalidad por adquirir la nacionalidad del país donde
          emigró).
        </li>
      </ul>
      <p>
        Las pautas, criterios y modelos de solicitud para tramitar el derecho de opción a la nacionalidad española en la
        disposición adicional octava de la Ley 19/2022, de 19 de octubre, de Memoria Democrática, están fijadas en la
        Instrucción del 25 de octubre de 2022, de la Dirección General de Seguridad Jurídica y Fe Publica.
      </p>
      <p>
        Las solicitudes se deben presentar a través de los Anexos correspondientes según el caso, de manera presencial
        en
        el registro civil más cercano a su domicilio en España o fuera de España en los consulados que corresponda según
        su lugar de residencia.
      </p>
      <p>
        Los modelos de solicitud se pueden obtener de manera telemática en la web del ministerio de justicia, del
        ministerio de asuntos exteriores o en el consulado al que pertenece según su lugar de residencia.
      </p>

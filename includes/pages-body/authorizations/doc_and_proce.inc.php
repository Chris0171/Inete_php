      <!-- ¿Cómo se realiza el Procedimiento?: -->
      <h2 class="mt-4">¿Cómo se realiza el Procedimiento?:</h2>
      <ul>
        <li>
          Sujeto legitimado para presentar la solicitud: El empleador o empresario, personalmente, o a través de un
          sujeto legitimado que tenga atribuida la representación legal empresarial.
          <ul>
            <li>
              <span class="fw-bold">Lugar de presentación:</span> Telemáticamente a través de la plataforma Mercurio
              Iniciales. <span class="fw-bold"><a
                  href="https://sede.administracionespublicas.gob.es/mercurio/inicioMercurio.html"
                  target="_blank">[Aquí]</a></span>.
            </li>
          </ul>
        </li>
        <li>
          Las tasas de residencia y trabajo: se devengarán en el momento de admisión a trámite de la solicitud, y
          deberán abonarse en el plazo de diez días hábiles, son:
          <ul>
            <li>
              Modelo 790 código 052, epígrafe 2.1 autorización inicial de residencia temporal a abonar por el
              trabajador extranjero.
            </li>
            <li>
              Modelo 790 código 062, a abonar por el empleador:
              <ul>
                <li>
                  Retribución inferior a 2 veces el SMI: epígrafe 1.1 por cuenta ajena con autorización de residencia
                </li>
                <li>
                  Retribución igual o superior a 2 veces el SMI.: epígrafe 1.6 por cuenta ajena con autorización de
                  residencia
                </li>
                <li>
                  El impreso para el abono se podrá descargar desde el portal de Internet de la Secretaria de Estado
                  de la función Pública <span class="fw-bold"><a
                      href="https://sede.administracionespublicas.gob.es/procedimientos/index/categoria/33"
                      target="_blank">[Aquí]</a></span>.
                </li>
              </ul>
            </li>
          </ul>
        </li>
        <li>
          El plazo de resolución de la solicitud es de 3 meses contados a partir del día siguiente a la fecha en que
          haya tenido entrada en el registro del órgano competente para su tramitación. Transcurrido dicho plazo sin que
          la Administración haya practicado la notificación, se podrá entender que la solicitud ha sido desestimada por
          silencio administrativo. (Cuando no se haya podido notificar la resolución se anunciará en el Tablón Edictal
          Único (TEU) <span class="fw-bold"><a href="https://www.boe.es/notificaciones/"
              target="_blank">[Aquí]</a></span>. Si se ha optado por notificación electrónica o normativamente
          estuviera obligado a utilizar ese medio, la resolución se notificará mediante publicación en la sede
          electrónica. Si no se accediera a la resolución en 10 días naturales desde su publicación, se dará por
          notificada).
        </li>
        <li>
          En el caso de concesión, el trabajador dispone de un mes desde la notificación al empleador, para solicitar
          personalmente el visado en la misión diplomática u oficina consular en cuya demarcación resida. A la solicitud
          de visado deberá acompañar lo siguiente:
          <ul>
            <li>
              o Pasaporte ordinario o título de viaje reconocido como válido en España con una vigencia mínima de cuatro
              meses.
            </li>
            <li>
              Certificado de antecedentes penales expedido por las autoridades del país de origen o del país o países en
              que haya residido durante los últimos cinco años.
            </li>
            <li>
              Certificado médico.
            </li>
            <li>
              Copia del contrato presentado y sellado por la Oficina de Extranjería.
            </li>
            <li>
              Justificante de haber abonado la tasa del visado.
            </li>
          </ul>
        </li>
        <li>
          La misión diplomática resolverá la solicitud del visado en el plazo de un mes.
        </li>
        <li>
          Notificada, en su caso, la concesión del visado, el trabajador deberá recogerlo personalmente en el plazo de
          un mes desde la fecha de notificación. De no efectuarse la recogida en el plazo mencionado, se entenderá que
          ha renunciado y se procederá a archivar el expediente.
        </li>
        <li>
          El trabajador deberá entrar a España durante los tres meses de vigencia del visado, que habilita para la
          entrada y permanencia en España en situación de estancia hasta que se produzca su afiliación y alta en el
          régimen correspondiente de Seguridad Social, dentro del plazo de tres meses desde la entrada.
        </li>
        <li>
          En el plazo de un mes desde que el trabajador se haya dado de alta en Seguridad Social, deberá solicitar,
          personalmente, la Tarjeta de Identidad de Extranjeros en la Oficina de Extranjería o Comisaría de Policía de
          la provincia donde se haya tramitado la autorización. Donde dirigirse, horario… <span class="fw-bold"><a
              href="https://mpt.gob.es/portal/delegaciones_gobierno/extranjeria/extranjeria_ddgg.htm"
              target="_blank">[Enlace Aquí]</a></span>.
        </li>
        <li>
          El solicitante exhibirá en el momento del trámite de huella su pasaporte o título de viaje y aportará:
          <ul>
            <li>
              Solicitud de tarjeta de estudiante extranjero, en modelo oficial (EX17). <span class="fw-bold"><a
                  href="https://www.inclusion.gob.es/web/migraciones/modelos-generales" target="_blank">[Descargar
                  Modelo Aquí]</a></span>.
            </li>
            <li>
              Justificante del abono de la tasa de la tarjeta.
            </li>
            <li>
              Una fotografía reciente en color, en fondo blanco, tamaño carné.
            </li>
            <li>
              Acreditación de la afiliación y/o alta de la Seguridad Social.
            </li>
          </ul>
        </li>
      </ul>


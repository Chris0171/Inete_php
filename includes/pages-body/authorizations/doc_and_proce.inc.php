      <h1>Documentación Exigible: </h1>
      <p>
        <span class="fw-bold">Es importante aportar copias de los documentos y exhibir los originales en el momento de
          presentar la solicitud.</span>
      </p>
      <ul>
        <li>
          Solicitud impreso el <span class="fw-bold">modelo oficial (EX–03)</span> por duplicado, debidamente
          cumplimentado y firmado por la empresa que contrata.
        </li>
      </ul>

      <br>

      <p>
        <span class="fw-bold">Documentación referida al trabajador:</span>
      </p>
      <ul>
        <li>
          Copia completa del pasaporte o título de viaje en vigor del trabajador.
        </li>
        <li>
          Copia de la documentación que acredite poseer la capacitación y, en su caso, la cualificación profesional
          legalmente exigida para el ejercicio de la profesión, debidamente homologada, según sea el caso.
        </li>
      </ul>

      <br>

      <p class="fw-bold">
        Documentación referida a la empresa, con excepción del servicio doméstico:
      </p>
      <ul>
        <li>
          Documentación de identificación de la empresa que solicita la autorización:
          <ul>
            <li>
              En caso de tratarse de un empresario individual: copia del NIF o NIE, o consentimiento para comprobar
              los datos de identidad a través del Sistema de Verificación de Datos de Identidad y Residencia.
            </li>
            <li>
              En caso de tratarse de una persona jurídica (S.A., S.L., Cooperativa, etc.):
              <ul>
                <li>
                  Copia del NIF de la empresa y copia de la escritura de constitución debidamente inscrita en el
                  Registro correspondiente.
                </li>
                <li>
                  Copia del documento público que acredite que el firmante de la solicitud de autorización ostenta la
                  representación legal de la empresa.
                </li>
                <li>
                  Copia del NIF o NIE o el consentimiento para comprobar los datos de identidad a través del Sistema
                  de Verificación de Datos de Identidad y Residencia del firmante de la solicitud.
                </li>
              </ul>
            </li>
          </ul>
        </li>
        <li>
          Documentación que acredite que la empresa puede garantizar la solvencia necesaria, ya sea a través de: copia
          de la declaración del IRPF, o del IVA, o del Impuesto de Sociedades o del informe de la vida laboral de la
          empresa (VILE), referido a los últimos tres años. También, debe aportarse una memoria descriptiva de la
          ocupación a realizar.
        </li>
      </ul>

      <br>

      <p class="fw-bold">
        Documentación del empleador si la actividad es del servicio doméstico:
      </p>
      <ul>
        <li>
          Copia del NIF ó NIE del empleador o el consentimiento para comprobar los datos de identidad a través del
          Sistema de Verificación de Datos de Identidad y Residencia.
        </li>
        <li>
          Copia de la última declaración del IRPF o la certificación de los ingresos emitida por la Administración
          Tributaria u otra documentación que acredite solvencia suficiente para la contratación.
        </li>
      </ul>

      <br>

      <p class="fw-bold">
        Documentación referida a la relación laboral:
      </p>
      <ul>
        <li>
          Contrato de trabajo firmado. Se deberá presentar original y copia. La copia será sellada por la Oficina de
          Extranjería y devuelta para su posterior presentación por el extranjero junto a la solicitud de visado de
          residencia y trabajo.
        </li>
        <li>
          Documentación acreditativa de concurrir algún supuesto de no consideración de la situación nacional de empleo.
          (Ver hoja informativa con los supuestos y la documentación precisa).
        </li>
      </ul>

      <br>

      <p class="fw-bold text-danger">
        Muy Importante:
      </p>
      <ul>
        <li>
          Cuando se aporten documentos de otros países deberán estar traducidos al castellano o lengua cooficial del
          territorio donde se presente la solicitud.
        </li>
        <li>
          Todo documento público extranjero deberá ser previamente legalizado por la Oficina consular de España con
          jurisdicción en el país en el que se ha expedido dicho documento o, en su caso, por el Ministerio de Asuntos
          Exteriores, Unión Europea y de Cooperación excepto en el caso en que dicho documento haya sido apostillado por
          la Autoridad competente del país emisor según el Convenio de la Haya de 5 de octubre de 1961 y salvo que dicho
          documento esté exento de legalización en virtud de Convenio Internacional.
        </li>
      </ul>


      <h1>Autorización inicial de residencia de trabajo por cuenta propia</h1>
      <p>
        Este tipo de autorización es de residencia temporal y trabajo a un extranjero no residente en España para la
        realización de una actividad lucrativa por cuenta propia.
      </p>
      <p>
        La información que se aporta a continuación es orientativa. Se debe aplicar lo dispuesto en la normativa vigente
        en el momento de presentación de la solicitud. <span class="fw-bold">Última actualización: enero 2025.</span>
      </p>

      <br>

      <p class="fw-bold">Normativas:</p>

      <ul>
        <li>
          <span class="fw-bold">Ley Orgánica 4/2000, de 11 de enero.</span> , sobre<span class="fw-bold"> Derechos y
            Libertades de los Extranjeros en España y su Integración Social (artículos 25,36 y 37)</span> <span
            class="fw-bold"><a href="https://www.boe.es/eli/es/lo/2000/01/11/4/con" target="_blank">[Aquí]</a></span>.
        </li>
        <li>
          <span class="fw-bold">Reglamento de la Ley Orgánica 4/2000</span>, aprobado por Real Decreto 557/2011, de 20
          de abril, (artículos 103 y 109) <span class="fw-bold"><a
              href="https://www.boe.es/eli/es/rd/2011/04/20/557/con" target="_blank">[Aquí]</a></span>.
        </li>
      </ul>

      <div class="separator"></div>

      <!-- Requisitos para la solicitud -->
      <h2 class="mt-4">Requisitos para la solicitud</h2>
      <ul>
        <li>
          <span class="fw-bold">No ser ciudadano de un Estado de la Unión Europea</span>, del Espacio Económico Europeo
          o de Suiza, o familiar de ciudadanos de estos países a los que les sea de aplicación el régimen de ciudadano
          de la Unión.
        </li>
        <li>
          <span class="fw-bold">No encontrarse irregularmente</span> en territorio español.
        </li>
        <li>
          <span class="fw-bold">Carecer de antecedentes penales en España</span> y en sus países anteriores de
          residencia, durante los últimos cinco años, por delitos existentes en el ordenamiento español.
        </li>
        <li>
          No encontrarse, en su caso, dentro del plazo de compromiso de no retorno a España que el extranjero haya
          asumido al retornar voluntariamente a su país de origen.
        </li>
        <li>
          <span class="fw-bold">No tener prohibida la entrada en España</span> y no figurar como rechazable en el
          espacio territorial de países con los que España tenga firmado un convenio en tal sentido.
        </li>
        <li>
          Debe cumplir con los requisitos que la legislación vigente exige a los nacionales para la apertura y
          funcionamiento de la actividad proyectada.
        </li>
        <li>
          Debe poseer la <span class="fw-bold">cualificación profesional</span> exigible o experiencia acreditada,
          suficiente en el ejercicio de la actividad profesional, así como en su caso la colegiación cuando así se
          requiera.
        </li>
        <li>
          Poder acreditar que la inversión prevista sea suficiente y la incidencia, en su caso, en la creación de
          empleo, incluyendo como tal el autoempleo.
        </li>
        <li>
          Debe haber abonado las tasas por tramitación de la autorización de residencia temporal y la relativa a la
          autorización de trabajo por cuenta propia.
        </li>
      </ul>


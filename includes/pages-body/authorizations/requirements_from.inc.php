      <h1>Estancia por estudios: Solicitud desde de españa</h1>


      <!-- Requisitos para la solicitud de estancia por estudios desde España -->
      <h2 class="mt-4">Requisitos de solicitud desde españa</h2>
      <ul>
        <li>
          <span class="fw-bold">No ser ciudadano de un Estado de la Unión Europea</span>, del Espacio Económico Europeo
          o de Suiza, o familiar de ciudadanos de estos países a los que les sea de aplicación el régimen de ciudadanos
          de la Unión.
        </li>
        <li>
          <span class="fw-bold">No figurar como rechazable</span> en el espacio territorial de los países con los que
          España haya firmado un convenio en tal sentido. Así como también no tener prohibida la entrada a España.
        </li>
        <li>
          Debe encontrarse <span class="fw-bold">regularmente en territorio español</span> y presentar la solicitud con
          una antelación mínima de un mes antes de la fecha de expiración de su situación.
        </li>
        <li>
          Haber abonado la tasa de tramitación del expediente.
        </li>
        <li>
          Contar con un <span class="fw-bold">seguro de enfermedad público o privado</span> acordado con una Entidad
          aseguradora autorizada para operar en España.
        </li>
        <li>
          Haber sido admitido en un <span class="fw-bold">centro de enseñanza autorizado en España</span>, para realizar
          un programa a tiempo completo, que conduzca a la obtención de un título o certificado de estudios.
        </li>
        <li>
          Poseer <span class="fw-bold">medios económicos suficientes</span> para sufragar los gastos de su estancia y
          regreso a su país de origen, y en su caso, los de sus familiares, de acuerdo con las cuantías siguientes:
        </li>
        <li>
          Para su manutención, mensualmente, el <span class="fw-bold">100 % del IPREM</span>, excepto si se acredita
          tener
          abonado de antemano el alojamiento por todo el tiempo que durara la estancia.
        </li>
        <li>
          Para el sostenimiento de sus familiares, mensualmente, el <span class="fw-bold">75% del IPREM</span> para el
          primer familiar y el 50% del IPREM para cada una de las demás personas, excepto que se acredite tener abonado
          de antemano el alojamiento por todo el tiempo que durara la estancia.
        </li>
        <li>
          No se contaran a estos efectos las cuantías utilizadas para financiar el coste de los estudios.
        </li>
      </ul>

      <br>

      <!-- En el caso de que la duración de la estancia supere los seis meses, se requerirá -->
      <h3>En el caso de que la duración de la estancia supere los seis meses, se requerirá:</h3>
      <ul>
        <li>
          No padecer ninguna de las enfermedades que pueden tener repercusiones de salud pública graves de conformidad
          con
          lo dispuesto en el Reglamento Sanitario Internacional de 2005.
        </li>
        <li>
          Cuando el solicitante se trate de un mayor de edad penal, <span class="fw-bold">carecer de antecedentes
            penales
            en España</span> y en sus países de residencia anteriores durante los últimos cinco años, por delitos
          existentes en el ordenamiento español.
        </li>
      </ul>

      <div class="separator"></div>

      <!-- Documentación que se debe aportar a la tramitación -->
      <h2>Documentación que se debe aportar a la tramitación</h2>
      <ul>
        <li>
          Prueba de que se encuentra regularmente en España.
        </li>
        <li>
          Prueba de que la presentación de la solicitud se realiza con un mes mínimo de antelación a la fecha de
          expiración de su situación.
        </li>
        <li>
          Impreso de solicitud en el modelo EX00, debidamente cumplimentado y firmado.
        </li>
        <li>
          Pasaporte en vigor, que sea válido en España, con vigencia mínima del periodo para el que se solicita la
          estancia.
        </li>
        <li>
          Documentación que acredite disponer de medios económicos necesarios para el período de estudios que se
          solicita y para el retorno al país de origen. se podrá aportar cualquier medio de prueba, como medios propios
          o procedentes de familiares, subvenciones, becas, ayudas, entre otros.
        </li>
        <li>
          Documentación que acredite disponer de seguro médico. Se considera válido también si la institución educativa
          conviene un seguro para el estudiante que abarque los riesgos cubiertos normalmente para los españoles.
        </li>
        <li>
          Documentación que acredite estar admitido en un centro de enseñanza, aportándose una carta de admisión o un
          certificado de matrícula, entre otras opciones similares.
        </li>
        <li>
          En el caso de estudiantes menores de edad, debe aportarse una autorización de padres o tutores en la que
          conste el centro, la entidad, organización y organismo responsable de la actividad y del periodo previsto de
          la estancia.
        </li>
      </ul>

      <!-- Si la duración de la estancia supera los seis meses debe aportarse -->
      <h3>Si la duración de la estancia supera los seis meses debe aportarse:</h3>
      <ul>
        <li>
          Certificado médico que acredite que no padece ninguna de las enfermedades con repercusiones en la salud
          pública internacional.
        </li>
        <li>
          Si es mayor de edad penal, debe aportar un certificado de antecedentes penales expedido por las autoridades
          del país de origen o del país en que haya residido durante los últimos cinco años.
        </li>
      </ul>

      <div class="separator"></div>

      <!-- Notas aclaratorias de importancia a tener en cuenta -->
      <h2>Notas aclaratorias de importancia a tener en cuenta:</h2>
      <ul>
        <li>
          En caso de aportarse documentos de otros países deberán estar traducidos al castellano o lengua cooficial del
          territorio donde se presente la solicitud.
        </li>
        <li>
          Todo documento público extranjero deberá legalizarse previamente por la Oficina consular de España en el país
          en el que se ha expedido dicho documento y según el caso, por el Ministerio de Asuntos Exteriores, Unión
          Europea y Cooperación excepto en el caso en que el documento sea apostillado por la Autoridad competente del
          país emisor según el Convenio de la Haya de 5 de octubre de 1961, o que dicho documento esté exento de
          legalización en virtud de Convenio Internacional.
        </li>
      </ul>

      <div class="separator"></div>

      <h2>Procedimiento de presentación de la solicitud de estancias por estudios desde España:</h2>
      <ul>
        <li>
          Se solicita personalmente por el interesado, por un representante o a través de los medios electrónicos.
          También la pueden solicitar la autorización de estancia, las instituciones de Educación Superior, si se va a
          realizar este tipo de estudios.
        </li>
        <li>
          El lugar de presentación puede ser a través de la plataforma de extranjería Mercurio. También, mediante cita
          previa en la Oficina de Extranjería de la provincia en la que esté situado el centro de enseñanza.
        </li>
      </ul>

      <!-- Autorización de estancia por estudios -->
      <h1>Estancia por estudios: Solicitud desde fuera de españa</h1>

      <p>En todos los casos siempre debe consultarse toda la información en la página web del consulado donde se
        presentará la solicitud del visado.</p>

      <!-- Requisitos para la solicitud de estancia por estudios desde España -->
      <h2 class="mt-4">Requisitos para la solicitud de visados de estudio</h2>
      <ul>
        <li>
          Pasaporte en vigor, que sea válido en España, con vigencia mínima del periodo para el que se solicita la
          estancia y debe tener dos páginas en blanco.
        </li>
        <li>
          Probar que reside en la demarcación consular donde realiza su solicitud de visado.
        </li>
        <li>
          Contar con un seguro de enfermedad público o privado acordado con una Entidad aseguradora autorizada para
          operar en España.
        </li>
        <li>
          Haber sido admitido en un centro de enseñanza autorizado en España, para realizar un programa a tiempo
          completo, que conduzca a la obtención de un título o certificado de estudios.
        </li>
        <li>
          En el caso de estudiantes menores de edad que no vengan acompañados de sus padres o tutores, debe aportar una
          autorización de éstos, con una constancia del centro, entidad, organización, y organismo responsable de la
          actividad y del periodo de estancia de estudio previsto.
        </li>
        <li>
          Poseer medios económicos suficientes para sufragar los gastos de su estancia y regreso a su país de origen, y
          en su caso, los de sus familiares, de acuerdo con las cuantías siguientes:
        </li>
        <ul>
          <li>
            Para su manutención, mensualmente, el 100 % del IPREM, excepto si se acredita tener abonado de antemano el
            alojamiento por todo el tiempo que durara la estancia.
          </li>
          <li>
            Para el sostenimiento de sus familiares, mensualmente, el 75% del IPREM para el primer familiar y el 50% del
            IPREM para cada una de las demás personas, excepto que se acredite tener abonado de antemano el alojamiento
            por todo el tiempo que durara la estancia.
          </li>
        </ul>
      </ul>

      <br>

      <!-- En el caso de que la duración de la estancia supere los seis meses, se requerirá -->
      <h3>En el caso de que la duración de la estancia supere los seis meses, se requerirá:</h3>
      <ul>
        <li>
          No padecer ninguna de las enfermedades que pueden tener repercusiones de salud pública graves de conformidad
          con lo dispuesto en el Reglamento Sanitario Internacional de 2005.
        </li>
        <li>
          Cuando el solicitante se trate de un mayor de edad penal, carecer de antecedentes penales en España y en sus
          países de residencia anteriores durante los últimos cinco años, por delitos existentes en el ordenamiento
          español.
        </li>
      </ul>


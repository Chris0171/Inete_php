      <h1>Autorización de estancia por estudios</h1>
      <p>
        Una autorización de estancia por estudios es lo que faculta a permanecer en España por un período superior a los
        noventa días. En este caso para la realización o ampliación de estudios en un centro de enseñanza autorizado en
        España, con un programa a tiempo completo, que conduzca a la obtención de un título o certificado de estudios.
      </p>
      <p>
        El contenido que se muestra a continuación es orientativo. En el momento de presentar la solicitud, se debe
        consultar y aplicar lo dispuesto en la normativa vigente.
      </p>

      <h2>Normativa:</h2>
      <ul>
        <li>
          <span class="fw-bold">Directiva (UE) 2016/801</span>, del Parlamento Europeo y del Consejo, de fecha 11 de
          mayo de 2016. Es la directiva concerniente a los requisitos de entrada y de residencia de los nacionales de
          terceros países con fines de estudios, investigación, prácticas, voluntariado, programas de intercambio de
          alumnos o proyectos educativos y colocación au pair.
        </li>
        <li>
          <span class="fw-bold">Reglamento de la Ley Orgánica 4/2000</span>, aprobado por el Real Decreto 557/2011, del
          20 de abril (artículos del 37 al 42).
        </li>
        <li>
          <span class="fw-bold">Ley Orgánica 4/2000 de fecha 11 de enero</span> sobre derechos y libertades de los
          extranjeros en España y su integración social (artículos 25 bis. f, 30 y 33).
        </li>
        <li>
          <span class="fw-bold">Instrucción DGM 2/2018</span>, sobre la transposición al ordenamiento jurídico español
          de la Directiva de 2016/801.
        </li>
      </ul>


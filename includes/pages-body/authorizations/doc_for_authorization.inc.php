      <!-- Documentación para la tramitación de la autorización -->
      <h1>Autorización de estancia por estudios (Documentación)</h1>
      <ul>
        <li>
          Prueba de que se encuentra regularmente en España.
        </li>
        <li>
          Prueba de que la presentación de la solicitud se realiza con un mes mínimo de antelación a la fecha de
          expiración de su situación.
        </li>
        <li>
          Impreso de solicitud en el <span class="fw-bold">modelo EX00</span>, debidamente cumplimentado y firmado.
        </li>
        <li>
          Pasaporte en vigor, que sea válido en España, con vigencia mínima del periodo para el que se solicita la
          estancia.
        </li>
        <li>
          Documentación que acredite disponer de medios económicos necesarios para el período de estudios que se
          solicita
          y para el retorno al país de origen. se podrá aportar cualquier medio de prueba, como medios propios o
          procedentes de familiares, subvenciones, becas, ayudas, entre otros.
        </li>
        <li>
          Documentación que acredite disponer de medios económicos necesarios para el período de estudios que se
          solicita
          y para el retorno al país de origen. se podrá aportar cualquier medio de prueba, como medios propios o
          procedentes de familiares, subvenciones, becas, ayudas, entre otros.
        </li>
        <li>
          Documentación que acredite estar admitido en un centro de enseñanza, aportándose una carta de admisión o un
          certificado de matrícula, entre otras opciones similares.
        </li>
        <li>
          En el caso de estudiantes menores de edad, debe aportarse una autorización de padres o tutores en la que
          conste
          el centro, la entidad, organización y organismo responsable de la actividad y del periodo previsto de la
          estancia.
        </li>
      </ul>
      <p class="fw-bold">Si la duración de la estancia supera los seis meses debe aportarse:</p>
      <ul>
        <li>
          Certificado médico que acredite que no padece ninguna de las enfermedades con repercusiones en la salud
          pública
          internacional.
        </li>
        <li>
          Si es mayor de edad penal, debe aportar un certificado de antecedentes penales expedido por las autoridades
          del
          país de origen o del país en que haya residido durante los últimos cinco años.
        </li>
      </ul>

      <div class="separator"></div>

      <!-- Notas aclaratorias de importancia a tener en cuenta -->
      <h2 class="mt-4">Notas aclaratorias de importancia a tener en cuenta: </h2>
      <ul>
        <li>
          En caso de aportarse documentos de otros países deberán estar traducidos al castellano o lengua cooficial del
          territorio donde se presente la solicitud.
        </li>
        <li>
          Todo documento público extranjero deberá legalizarse previamente por la Oficina consular de España en el país
          en
          el que se ha expedido dicho documento y según el caso, por el Ministerio de Asuntos Exteriores, Unión Europea
          y
          Cooperación excepto en el caso en que el documento sea apostillado por la Autoridad competente del país emisor
          según el Convenio de la Haya de 5 de octubre de 1961, o que dicho documento esté exento de legalización en
          virtud de Convenio Internacional.
        </li>
      </ul>

      <div class="separator"></div>

      <!-- Procedimiento de presentación de la solicitud de estancias por estudios desde España -->
      <h2>Procedimiento de presentación de la solicitud de estancias por estudios desde España:</h2>
      <ul>
        <li>
          Se solicita personalmente por el interesado, por un representante o a través de los medios electrónicos.
          También
          la pueden solicitar la autorización de estancia, las instituciones de Educación Superior, si se va a realizar
          este tipo de estudios.
        </li>
        <li>
          El lugar de presentación puede ser a través de la plataforma de extranjería <span
            class="fw-bold">Mercurio</span>. También, mediante cita previa en la Oficina de Extranjería de la provincia
          en
          la que esté situado el centro de enseñanza.
        </li>
      </ul>


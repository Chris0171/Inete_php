      <!-- ¿Cómo se realiza el Procedimiento?: -->
      <h2 class="mt-4">¿Cómo se realiza el Procedimiento?</h2>
      <ul>
        <li>
          Sujeto legitimado para presentar esta solicitud: el extranjero, personalmente.
        </li>
        <li>
          Lugar de presentación: misión diplomática u oficina consular española correspondiente a su lugar de
          residencia.
        </li>
        <li>
          Tasas de residencia y de trabajo: se devengarán en el momento de admisión a trámite de la solicitud, y
          deberán abonarse en el plazo de diez días hábiles, son:
          <ul>
            <li>
              Modelo 790 código 052, epígrafe 2.1 Autorización inicial de residencia temporal <span class="fw-bold"><a
                  href="https://sede.administracionespublicas.gob.es/procedimientos/index/categoria/33?msclkid=da3ac073bef411ecb4dbb7f8fd4e4f36"
                  target="_blank">[Descargar Modelo Aquí]</a></span>.
            </li>
            <li>
              Modelo 790 Código 062, epígrafe 1.5 Autorizaciones de trabajo por cuenta propia <span class="fw-bold"><a
                  href="https://sede.administracionespublicas.gob.es/procedimientos/index/categoria/33?msclkid=da3ac073bef411ecb4dbb7f8fd4e4f36"
                  target="_blank">[Descargar Modelo Aquí]</a></span>.
            </li>
          </ul>
        </li>
        <li>
          El plazo de resolución de la solicitud es de 3 meses contados a partir del día siguiente a la fecha en que
          haya tenido entrada en el registro del órgano competente para su tramitación. Transcurrido dicho plazo sin que
          la Administración haya practicado la notificación, se podrá entender que la solicitud ha sido desestimada por
          silencio administrativo.
        </li>
        <li>
          La Misión diplomática u oficina consular notificará al interesado la resolución sobre la solicitud de
          residencia temporal y trabajo por cuenta propia.
        </li>
        <li>
          En el caso de concesión, el trabajador dispone de un mes desde la notificación para solicitar personalmente
          el visado, en la misión diplomática u oficina consular en cuya demarcación resida. A la solicitud deberá
          acompañar:
          <ul>
            <li>
              <span class="fw-bold">Pasaporte</span> ordinario o título de viaje reconocido como válido en España con
              una <span class="fw-bold">vigencia mínima de cuatro meses</span>.
            </li>
            <li>
              <span class="fw-bold">Certificado de antecedentes penales</span> expedido por las autoridades del país de
              origen o del país o países en que haya residido durante los últimos cinco años.
            </li>
            <li>
              <span class="fw-bold">Certificado médico.</span>
            </li>
          </ul>
        </li>
        <li>
          La misión diplomática resolverá la solicitud del visado en el <span class="fw-bold">plazo de un mes</span>.
        </li>
        <li>
          <span class="fw-bold">Notificada la concesión</span> del visado, en su caso, el trabajador deberá <span
            class="fw-bold">recogerlo personalmente en el plazo de un mes</span> desde la fecha de notificación. De no
          efectuarse la recogida en el plazo mencionado, se entenderá que ha renunciado y se producirá el archivo del
          expediente.
        </li>
        <li>
          Una vez recogido el visado, el trabajador deberá entrar en territorio español en el plazo de vigencia del
          visado, que será de tres meses, el cual habilita para la entrada y permanencia en España en situación de
          estancia hasta que se produzca su afiliación y alta en la Seguridad Social. A partir de la entrada en España,
          el trabajador dispone de tres meses para que se efectúe su afiliación, alta y posterior cotización en los
          términos establecidos por la normativa de Seguridad Social que resulte de aplicación.
        </li>
        <li>
          En el plazo de un mes desde que el trabajador se haya dado de alta en Seguridad Social, deberá solicitar,
          personalmente, la Tarjeta de Identidad de Extranjeros en la Oficina de Extranjería o Comisaría de Policía de
          la provincia donde se haya tramitado la autorización. Donde dirigirse, horario... <span class="fw-bold"><a
              href="https://mpt.gob.es/portal/delegaciones_gobierno/extranjeria/extranjeria_ddgg.html"
              target="_blank">[Enlace Aquí]</a></span>
        </li>
        <li>
          El solicitante exhibirá en el momento del trámite de huella, su pasaporte o título de viaje y aportará lo
          siguiente:
          <ul>
            <li>
              Solicitud de tarjeta en modelo oficial (EX-17) <span class="fw-bold"><a
                  href="https://www.inclusion.gob.es/web/migraciones/modelos-generales" target="_blank">[Descargar
                  Modelo Aquí]</a></span>.
            </li>
            <li>
              Justificante del abono de la tasa de la tarjeta.
            </li>
            <li>
              Una fotografía reciente en color, en fondo blanco, tamaño carné.
            </li>
            <li>
              Acreditación de la afiliación y/o alta de la Seguridad Social.
            </li>
          </ul>
        </li>
      </ul>


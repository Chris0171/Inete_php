      <!-- Permiso para trabajar por cuenta ajena -->
      <h1>Permiso para trabajar por cuenta ajena</h1>

      <br>

      <!-- Requisitos -->
      <h2 class="mt-4">Requisitos para trabajar por cuenta ajena</h2>
      <ul>
        <li>
          Las actividades laborales deberán ser compatibles con la realización de los estudios y no superar las 30 horas
          semanales.
        </li>
        <li>
          No será preciso solicitar autorización para aquellas prácticas no laborales en entidades públicas o privadas
          que formen parte del plan de estudios para el que se otorgó la autorización de estancia y que se produzcan en
          el marco de los correspondientes convenios de colaboración entre dichas entidades y el centro docente o
          científico de que se trate.
        </li>
        <li>
          Cuando se trate de estudios superiores, de una formación reglada para el empleo o destinada a la obtención de
          un certificado de profesionalidad, o una formación conducente a la obtención de la certificación de aptitud
          técnica o habilitación profesional necesaria para el ejercicio de una ocupación específica, la autorización de
          estancia por estudios autorizará a trabajar por cuenta ajena y por cuenta propia sin necesidad de solicitud de
          una autorización expresa.
        </li>
      </ul>

      <div class="separator"></div>

      <!-- Documentación Exigible -->
      <h2>Documentación Exigible</h2>
      <ul>
        <li>
          Impreso de solicitud en modelo oficial (EX-12) por duplicado, debidamente cumplimentado y firmado por el
          empleador.
        </li>
        <li>
          Pasaporte completo o documento de viaje en vigor del trabajador.
        </li>
        <li>
          Documentación acreditativa de poseer la capacitación y, en su caso, la cualificación profesional legalmente
          exigida para el ejercicio de la profesión así como titulación homologada y colegiación, cuando sean
          necesarias.
        </li>
        <li>
          Documentación acreditativa de la compatibilidad de los estudios con la actividad laboral.
        </li>
        <li>
          Documentación que identifique a la empresa que solicita la autorización:
          <ul>
            <li>
              Si se trata de un empresario individual: NIF ó NIE o consentimiento para comprobar los datos de identidad
              a través del Sistema de Verificación de Datos de Identidad y Residencia.
            </li>
          </ul>
        </li>
        <li>
          Si se trata de una persona jurídica (S.A., S.L., Cooperativa, etc.):
          <ul>
            <li>
              NIF de la empresa y copia de la escritura de constitución debidamente inscrita en el Registro
              correspondiente.
            </li>
            <li>
              Documento público que acredite que el firmante de la solicitud de autorización ostenta la representación
              legal de la empresa.
            </li>
            <li>
              NIF o NIE del firmante de la solicitud o consentimiento para comprobar los datos de identidad a través del
              Sistema de Verificación de Datos de Identidad y Residencia.
            </li>
            <li>
              Contrato de trabajo firmado por el empleador y el trabajador.
            </li>
            <li>
              Documentación acreditativa de los medios económicos para hacer frente a las obligaciones contractuales.
            </li>
          </ul>
        </li>
      </ul>

      <div class="separator"></div>

      <!-- Procedimiento -->
      <h2>Procedimiento</h2>

      <ul>
        <li>
          Sujeto legitimado para presentar la solicitud: el empleador o empresario, personalmente, o a través de un
          sujeto legitimado que tenga atribuida la representación legal empresarial.
        </li>
        <li>
          Lugar de presentación: Oficina de Extranjería de la provincia donde se vayan a prestar los servicios. La
          información de la dirección, teléfonos y horarios de atención al público se puede consultar en Información de
          la dirección, teléfonos y horarios de atención al público.Enlace externo al portal. Se abrirá en una pestaña
          nueva.
        </li>
        <li>
          Las tasas de trabajo: se devengarán en el momento de admisión a trámite de la solicitud y deberán abonarse por
          el empleador en el plazo de diez días hábiles.
        </li>
        <li>
          Modelo 790 código 062, disponible en este enlaceEnlace externo al portal. Se abrirá en una pestaña nueva.
        </li>
        <li>
          <span class="fw-bold">Plazo de resolución de la solicitud:</span> 3 meses contados a partir del día siguiente
          a la fecha en que haya tenido entrada en el registro del órgano competente para su tramitación. Transcurrido
          dicho plazo sin que la Administración haya practicado la notificación, se podrá entender que la solicitud ha
          sido desestimada por silencio administrativo (cuando no se haya podido notificar la resolución se anunciará en
          el Tablón Edictal Único (TEU)Enlace externo al portal. Se abrirá en una pestaña nueva.. Si se hubiera optado
          por notificación electrónica o normativamente estuviera obligado a utilizar ese medio, la resolución se
          notificará mediante publicación en la sede electrónica. Si no se accediera a la resolución en 10 días
          naturales desde su publicación, se dará por notificada).
        </li>
        <li>
          La vigencia de la autorización para trabajar coincidirá con la duración de la autorización de estancia. La
          pérdida de vigencia de la autorización de estancia será causa de extinción de la autorización para trabajar.
        </li>
        <li>
          La autorización para trabajar se prorrogará si subsisten las circunstancias que motivaron la concesión
          anterior, siempre y cuando se haya obtenido la prórroga de la autorización de estancia.
        </li>
        <li>
          Haber abonado la tasa de la autorización por cuenta ajena.
        </li>
      </ul>


      <!-- Autorización inicial de residencia de trabajo por cuenta ajena -->
      <h1>Autorización inicial de residencia de trabajo por cuenta ajena</h1>
      <p>
        La Ley de extranjería desde su entrada en vigor ha sufrido modificaciones, perfeccionando el sistema de
        canalización legal y ordenada de los flujos migratorios laborales, vinculando la capacidad de acogida de
        trabajadores inmigrantes a las necesidades del mercado de trabajo”.
      </p>
      <p>
        Es importante que se conozca que no se puede hacer cualquier oferta de trabajo o empleo, ya que estos están
        limitados por un catálogo de actividades de difícil cobertura que elabora el Servicio Público de Empleo y que se
        publica cada tres meses.
      </p>
      <p>
        <span class="fw-bold">Nota: </span>La información que a continuación se brinda es orientativa por lo que a la
        hora de hacer cualquier trámite o solicitud se debe aplicar lo dispuesto en las normativas vigentes en ese
        momento.
      </p>

      <br>

      <h4>¿Quién debe presentar la solicitud de autorización inicial de residencia y trabajo por cuenta ajena?</h4>
      <p>
        El sujeto legitimado para solicitar la autorización es el empleador o empresario que quiere contratarlo en
        España para un período superior a 90 días y menor de 5 años. La solicitud se realizará, si se trata de empleador
        persona física, personalmente, y si se trata de empleador persona jurídica, a través de quien tenga atribuida la
        representación legal de la entidad.
      </p>

      <div class="separator"></div>

      <!-- Requisitos para la solicitud -->
      <h2 class="mt-4">Requisitos para la solicitud</h2>
      <ul>
        <li>
          No ser ciudadano de un Estado de la Unión Europea, del Espacio Económico Europeo o de Suiza, o ser familiar
          de ciudadanos de estos países a los que les sea de aplicación el régimen de ciudadano de la Unión.
        </li>
        <li>
          No debe encontrarse irregularmente en territorio español si en el momento de la solicitud se encuentra en
          territorio español.
        </li>
        <li>
          Carecer de antecedentes penales durante los últimos 5 años, tanto en España como en países anteriores de su
          residencia por delitos existentes en el ordenamiento español.
        </li>
        <li>
          No tener prohibida la entrada en territorio español y no figurar como rechazable en el espacio territorial de
          países con los que España tenga firmado un convenio en tal sentido.
        </li>
        <li>
          No debe encontrarse, en su caso, dentro del plazo de compromiso de no retorno a España que el extranjero haya
          asumido al retornar de manera voluntaria a su país de origen.
        </li>
        <li>
          Haber abonado las tasas por tramitación de la autorización de residencia y la de trabajo por cuenta ajena.
        </li>
        <li>
          Que la situación nacional de empleo permita la contratación. Lo permite si:
          <ul>
            <li>
              La ocupación que va a desempeñar el trabajador en la empresa está incluida en el catálogo de ocupaciones
              de difícil cobertura.
            </li>
            <li>
              En las ocupaciones no calificadas como de difícil cobertura cuando el empleador acredite (ante la Oficina
              de Extranjería) la dificultad de cubrir los puestos de trabajo vacantes con trabajadores ya incorporados
              en el mercado laboral interno. A tales efectos, la Oficina de Extranjería tendrá en consideración el
              informe presentado por los Servicios Públicos de Empleo, así como la urgencia de la contratación
              acreditada por la empresa. Para ello se deberá presentar una oferta de empleo en el porta Empléate y los
              Servicios Públicos de Empleo.
            </li>
            <li>
              o La autorización está dirigida a nacionales de Estados con los que España haya suscrito acuerdos
              internacionales <span class="fw-bold">(Chile y Perú)</span>.
            </li>
            <li>
              o Pueden acreditar la concurrencia de un supuesto recogido en el <span class="fw-bold">artículo 40 de la
                Ley Orgánica 4/2000</span>.
            </li>
          </ul>
        </li>
        <li>
          Presentar un contrato firmado por el empleador y trabajador que garantice al trabajador una actividad
          continuada durante el periodo de vigencia de la autorización para residir y trabajar. La fecha estará
          condicionada al momento de eficacia de la autorización de residencia y trabajo por cuenta ajena.
        </li>
        <li>
          Las condiciones fijadas en el contrato de trabajo se deberán ajustar a las establecidas por la normativa
          vigente. En caso de que el contrato fuera a tiempo parcial, la retribución deberá ser igual o superior al
          salario mínimo interprofesional para jornada completa y en cómputo anual.
        </li>
        <li>
          El empleador solicitante debe estar inscrito en el sistema de la Seguridad Social y encontrarse al corriente
          del cumplimiento de sus obligaciones tributarias y frente a la Seguridad Social.
        </li>
        <li>
          El empleador deberá contar con medios económicos, materiales o personales suficientes para su proyecto
          empresarial y para hacer frente a las obligaciones asumidas en el contrato frente al trabajador.
        </li>
        <li>
          Si el empleador es una persona física deberá acreditar, una vez descontado el pago del salario convenido, el
          100% del IPREM si no hay familiares a su cargo.
        </li>
        <li>
          Si la unidad familiar incluye dos miembros el 200%. Si la unidad familiar incluye más de dos personas se
          deberá sumar a la cantidad anterior el 50 % del IPREM por cada miembro adicional
        </li>
        <li>
          Poseer la capacitación y, en su caso, la cualificación profesional legalmente exigida para el ejercicio de la
          profesión, en su caso debidamente homologada.
        </li>
      </ul>


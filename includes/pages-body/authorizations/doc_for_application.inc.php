      <!-- Documentación para la tramitación de la solicitud de visado -->
      <h1>Visado de estancia por estudios (Documentación)</h1>
      <ul>
        <li>
          Formulario de solicitud de visado nacional.
        </li>
        <li>
          Prueba de la residencia en la demarcación consular donde solicita el visado. El solicitante deberá acreditar
          su
          residencia legal en el país donde solicita su visado, presentando su documento de identidad vigente y una
          fotocopia del mismo.
        </li>
        <li>
          Fotografía reciente, tamaño carne, a color con fondo blanco. Debe estar tomado de frente, sin gafas, ni
          reflejos, ni prendas que oculten el rostro.
        </li>
        <li>
          Pasaporte en vigor, original y fotocopia de las páginas con datos biométricos. El pasaporte debe ser válido,
          como mínimo, para toda la duración de la estancia de estudios prevista y debe tener dos páginas en blanco.
        </li>
        <li>
          Prueba de la admisión para la actividad de estudios, formación, voluntariado o prácticas.
        </li>
        <li>
          Prueba de disponer de medios económicos. Original y una copia de los documentos que prueben que el solicitante
          (o su representante legal o o en su caso el familiar que le respalda) dispone de medios económicos suficientes
          para cubrir los gastos de la estancia y el regreso del estudiante, y en su caso, de los familiares que le
          acompañen.
        </li>
        <li>
          La cantidad mínima que se exige equivale al <span class="fw-bold">100% del IPREM</span>. A esta cantidad se le
          añade el <span class="fw-bold">75% el IPREM</span> por el primer familiar y el <span class="fw-bold">50% del
            IPREM</span> por cada familiar adicional. En caso de acreditar que el alojamiento ya está pagado por
          adelantado para toda la estancia, será menor esta cantidad.
        </li>
        <li>
          Prueba de un seguro de enfermedad. Original y una copia del certificado de seguro de enfermedad público o
          privado contratado con una entidad aseguradora autorizada a operar en España. El seguro debe cubrir todos los
          riesgos asegurados por el sistema público de salud en España. Si el solicitante participa en programa de
          voluntariado, además del seguro médico de enfermedad, debe aportar una copia de la póliza de un seguro de
          responsabilidad civil suscrito por la organización.
        </li>
      </ul>
      <p class="fw-bold">Cuando la duración de los estudios supera los 6 meses: </p>
      <ul>
        <li>
          <span class="fw-bold">Certificado médico (original y una copia)</span>, que acredite que el solicitante no
          padece enfermedades que puedan tener repercusiones graves para la salud pública de conformidad con el
          Reglamento
          Sanitario Internacional de 2005. El certificado debe presentarse debidamente legalizado por el MINREX.
        </li>
        <li>
          El solicitante mayor de edad penal, debe presentar <span class="fw-bold">original y copia del certificado de
            antecedentes penales</span> expedido por el país o países donde haya residido en los últimos 5 años. El
          certificado debe presentarse debidamente legalizado por el MINREX.
        </li>
        <li>
          Si el solicitante es menor de edad, debe presentar copia del documento de identidad o pasaporte del progenitor
          y
          del documento que pruebe el parentesco. Si se solicita el visado a través de representante, se presentará
          copia
          del documento de identidad o pasaporte del representante y del poder notarial o documento que acredite la
          representación. El documento debe presentarse en original y fotocopia y estar debidamente legalizado por el
          MINREX o apostillada, según sea el caso.
        </li>
        <li>
          <span class="fw-bold">Autorización de los representantes legales</span>. Si el solicitante es menor de edad,
          se
          debe aportar original y una copia del acta notarial de los progenitores o tutores autorizando el
          desplazamiento
          del menor a España e identificando a la persona que se hará cargo del menor en España. El documento debe
          presentarse en original y fotocopia y estar debidamente legalizado por el MINREX o apostillada, según sea el
          caso.
        </li>
        <li>
          Abono de la tasa del visado. Esto se hará el día de la cita, cada solicitante (en caso de acompañantes) deberá
          abonar la tasa del visado. Dicha tasa no será devuelta en caso de denegación del visado.
        </li>
      </ul>


      <!-- Procedimiento -->
      <h2>Procedimiento</h2>

      <ul>
        <li>
          Sujeto legitimado para presentar la solicitud: el estudiante extranjero personalmente.
        </li>
        <li>
          Lugar de presentación: Oficina de Extranjería de la provincia donde tenga fijado su domicilio. Información de
          la dirección, teléfonos y horarios de atención al público.Enlace externo al portal. Se abrirá en una pestaña
          nueva.
        </li>
        <li>
          Las tasas de trabajo se devengarán en el momento de admisión a trámite de la solicitud y deberán abonarse por
          el estudiante en el plazo de diez días hábiles.
        </li>
        <li>
          Modelo 790 código 062Enlace externo al portal. Se abrirá en una pestaña nueva.
        </li>
        <li>
          <span class="fw-bold">Plazo de resolución de la solicitud:</span> 3 meses contados a partir del día siguiente
          a la fecha en que haya tenido entrada en el registro del órgano competente para su tramitación. Transcurrido
          dicho plazo sin que la Administración haya practicado la notificación, se podrá entender que la solicitud ha
          sido desestimada por silencio administrativo (cuando no se haya podido notificar la resolución se anunciará en
          el Tablón Edictal Único (TEU)Enlace externo al portal. Se abrirá en una pestaña nueva. . Si se hubiera optado
          por notificación electrónica o normativamente estuviera obligado a utilizar ese medio, la resolución se
          notificará mediante publicación en la sede electrónica. Si no se accediera a la resolución en 10 días
          naturales desde su publicación, se dará por notificada).
        </li>
        <li>
          La vigencia de la autorización para trabajar coincidirá con la duración de la autorización de estancia. La
          pérdida de vigencia de la autorización de estancia será causa de extinción de la autorización para trabajar.
        </li>
        <li>
          La autorización para trabajar se prorrogará si subsisten las circunstancias que motivaron la concesión
          anterior, siempre y cuando se haya obtenido la prórroga de la autorización de estancia
        </li>
      </ul>


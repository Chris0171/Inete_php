      <h1>Permiso para trabajar por cuenta propia</h1>

      <br>

      <!-- Requisitos -->
      <h2 class="mt-4">Requisitos para trabajar por cuenta propia</h2>
      <ul>
        <li>
          Las actividades laborales deberán ser compatibles con la realización de los estudios y no superar las 30 horas
          semanales.
        </li>
        <li>
          Cumplir los requisitos que la legislación vigente exige para la apertura y funcionamiento de la actividad
          proyectada.
        </li>
        <li>
          Poseer la cualificación profesional exigible o experiencia acreditada suficiente en el ejercicio de la
          actividad profesional, así como en su caso, la colegiación cuando así se requiera. En su caso, la titulacion
          homologada.
        </li>
        <li>
          Poder acreditar que la inversión prevista sea suficiente y la incidencia, en su caso, en la creación de
          empleo,incluyendo como tal el autoempleo.
        </li>
        <li>
          Haber abonado la tasa relativa a la utorización de trabajo por cuenta propia.
        </li>
      </ul>

      <div class="separator"></div>

      <!-- Documentación Exigible -->
      <h2>Documentación Exigible</h2>
      <p>
        <span class="fw-bold">Nota:</span> Con carácter general se deberán aportar copias de los documentos y exhibir
        los originales en el momento de presentar la solicitud
      </p>
      <ul>
        <li>
          Impreso de solicitud en modelo oficial (EX-12) por duplicado, debidamente cumplimentado y firmado por el
          estudiante.
        </li>
        <li>
          Pasaporte completo o documento de viaje o en vigor.
        </li>
        <li>
          Relación de las autorizaciones o licencias que se exijan para la instalación, apertura o funcionamiento de la
          actividad proyectada o para el ejercicio de dicha profesión.
        </li>
        <li>
          Documentación acreditativa de la capacitación, y, en su caso, de la cualificación profesional legalmente
          exigida para el ejercicio de la profesión,en su caso, titulación homologada.
        </li>
        <li>
          Documentación acreditativa de la compatibilidad de los estudios con la actividad laboral.
        </li>
        <li>
          En actividades profesionales independientes que la exijan, certificado de colegiación.
        </li>
        <li>
          Copia de la documentación que acredite la suficiencia de la inversión prevista para la implantación del
          proyecto y sobre la incidencia, en su caso, en la creación de empleo, incluyendo como tal el autoempleo.
        </li>
        <li>
          En actividades comerciales minoristas y prestación de los servicios enumerados en el Anexo de la Ley 12/2012,
          que se realicen en establecimientos permanentes, cuya superficie útil sea igual o inferior a 750 metros
          cuadrados, declaración responsable o comunicación previa (en los términos del artículo 69 de la Ley 39/2015)
          y, en su caso, justificante de pago del tributo correspondiente.
        </li>
        <li>
          En el resto de las actividades y prestaciones de servicios profesionales, relación de las autorizaciones o
          licencias que se exijan para la instalación, apertura o funcionamiento de la actividad proyectada o para el
          ejercicio profesional, indicando, la situación en la que se encuentre los trámites para su consecución,
          incluyendo, en su caso, las certificaciones de solicitud ante los organismos correspondientes.
        </li>
        <li>
          Con independencia de su justificación mediante otros medios admisibles en Derecho, los dos últimos apartados
          pueden ser acreditados por el informe de valoración emitido por una de las siguientes organizaciones:
          <ul>
            <li>
              Federación Nacional de Asociaciones de Empresarios y Trabajadores Autónomos (ATA).
            </li>
            <li>
              Unión de Profesionales y Trabajadores Autónomos (UPTA).
            </li>
            <li>
              Confederación Intersectorial de Autónomos del Estado Español (CIAE).
            </li>
            <li>
              Organización de Profesionales y Autónomos (OPA).
            </li>
            <li>
              Unión de Asociaciones de Trabajadores Autónomos y Emprendedores (UATAE).
            </li>
          </ul>
        </li>
      </ul>


      <h2>Búsqueda de partida de bautismos españoles</h2>
      <ul>
        <li>
          Buscar en archivo parroquial o diocesano del lugar de nacimiento (municipio, o pueblo).
        </li>
        <li>
          Si no se tienen los datos completos del nacimiento del español, se puede contratar el servicio a un
          genealogista, es lo más recomendable ya que en las parroquias o archivos diocesanos, por lo general no hacen
          búsqueda si no se tienen los datos concretos del nacimiento como la fecha (día, mes y año) y lugar de
          nacimiento
          o parroquia donde fue bautizado.
        </li>
        <li>
          Los bautismos realizados después de 1871, deben presentarse acompañados de la negativa de nacimiento del
          registro civil ya que a partir de esta fecha se crearon dichos registros.
        </li>
        <li>
          Los bautismos realizados antes de 1871, no es obligatorio presentar la negativa de Nacimiento del español ya
          que hasta esta fecha no se habían creado los registros civiles en España.
        </li>
      </ul>
      <p>
        Los genealogistas disponen de mucha información, habilidad de búsqueda y acceso a las Diócesis y parroquias y
        por lo general no cobran el servicio si no encuentran el bautismo.
      </p>


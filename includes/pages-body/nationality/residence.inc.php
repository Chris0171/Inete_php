      <!-- Nacionalidad española por residencia -->
      <h1>Nacionalidad española por residencia</h1>
      <p>
        Una de las formas de obtener la nacionalidad española, es por residencia.
        La información que se proporciona es solo orientativa y se debe buscar asesoramiento específico con un experto o
        consultar en el ministerio de justicia.
      </p>
      <!-- Documentación a aportar para el caso de Anexo I -->
      <h2 class="mt-4">Requisitos para solicitar la nacionalidad española por residencia:</h2>
      <ol>
        <li>
          <p>
            Residir en España de forma legal: Se debe tener una autorización de residencia vigente. La <span
              class="fw-bold">autorización de estancia por estudios</span> es un permiso de residencia que no cuenta
            para la nacionalidad española.
          </p>
        </li>
        <li>
          <p>
            Se debe demostrar una buena conducta cívica en España. Demostrando que se carece de antecedentes penales o
            policiales en España y en su país de origen.
          </p>
        </li>
        <li>
          <p>
            Demostrar conocimiento de la cultura española y la integración en España. Esto se demuestra con el examen
            <span class="fw-bold">CCEE</span> de conocimientos generales de la cultura española del <span
              class="fw-bold">Instituto de Cervantes</span>. Si en su país de origen el español no es un idioma oficial,
            debe demostrar además que tiene un nivel de español básico a través de la prueba <span class="fw-bold">DELE
              A2</span>, que se imparte por el <span class="fw-bold">Instituto cervantes</span>.
          </p>
        </li>
        <li>
          <p>
            Se debe haber residido en un periodo de tiempo de 10 años en España de forma legal y continuada,
            inmediatamente antes de la fecha en la que solicites tu nacionalidad. Esto cambia para algunas personas
            según
            los casos siguientes:
          </p>
          <ul>
            <li>
              <p>
                <span class="fw-bold">Cinco años:</span>
              </p>
              <ul>
                <li>
                  Para aquellas personas que hayan obtenido la condición de refugiado.
                </li>
              </ul>
            </li>
            <li>
              <p><span class="fw-bold">Dos años:</span></p>
              <ul>
                <li>
                  Para los nacionales de países iberoamericanos <a
                    href="https://www.igae.pap.hacienda.gob.es/sitios/igae/es-ES/QuienesSomos/Presentacion/paisesiberoamericanos/Paginas/paisesiberoamericanos.aspx"
                    target="_blank" class="fw-bold instructions">[ver aquí]</a>, Portugal, Andorra, Filipinas, Guinea
                  Ecuatorial y personas de origen sefardí.
                </li>
              </ul>
            </li>
            <li>
              <p><span class="fw-bold">Un año:</span></p>
              <ul>
                <li>
                  Si lleva un año casado/a con un ciudadano/a español y su matrimonio se encuentra inscrito en España.
                </li>
                <li>
                  Si se ha nacido en españa.
                </li>
                <li>
                  Si ha estado bajo la tutela o protección de un centro de menores español.
                </li>
                <li>
                  Si no ejerció su derecho de opción a obtener la nacionalidad española.
                </li>
                <li>
                  Si es viudo o viuda de española o español, si en el momento de la muerte del cónyuge no estaban
                  separados
                  de hecho o judicialmente.
                </li>
                <li>
                  El nacido fuera de España de padre o madre, (nacidos también fuera de España), abuelo o abuela, que
                  hubieran sido originariamente españoles.
                </li>
              </ul>
            </li>
          </ul>
        </li>
      </ol>


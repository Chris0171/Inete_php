      <h2 class="mt-4">Presentación de solicitudes:</h2>
      <ul>
        <li>
          Las solicitudes se pueden hacer Online a través de la sede electrónica del Ministerio de justicia, donde se
          asigna número de expediente, desde el mismo momento de presentada la solicitud, y con el mismo se puede hacer
          seguimiento de la misma a través de la Web del ministerio de justicia. <span class="fw-bold"><a
              href="https://sede.mjusticia.gob.es/eConsultas/inicioNacionalidad" target="_blank">[Aquí]</a></span>
        </li>
        <li>
          De manera presencial en cualquiera de las entidades a las que se refiere el artículo 16 de la Ley 39/2015, de
          1
          de octubre. Presentar la solicitud por el registro tiene el inconveniente de que no se asigna un número de
          expediente en el momento de presentar, si no que se debe esperar a que el Ministerio lo asigne, y esto puede
          tardar un tiempo considerable.
        </li>
      </ul>


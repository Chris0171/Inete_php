      <!-- Documentación para casos especificos -->
      <h1>Documentos adicionales que deben aportarse en casos específicos:</h1>

      <br>

      <p class="fw-bold">Refugiados:</p>
      <ul>
        <li>
          Tarjeta de Identidad de extranjeros (TIE) donde conste su condición de refugiado.
        </li>
        <li>
          Pasaporte Azul de la Convención de Ginebra de 1951, si dispone del mismo.
        </li>
        <li>
          Certificado de la Oficina de Asilo y Refugio del Ministerio del Interior con una validez de seis meses desde
          la
          fecha de su expedición, donde conste:
          <ul>
            <li>Nombre y apellidos.</li>
            <li>Fecha y lugar de nacimiento.</li>
            <li>Nombre de los padres.</li>
            <li>Probar que mantiene su condición de refugiado.</li>
          </ul>
        </li>
      </ul>

      <br>

      <p class="fw-bold">Nacidos en territorio español:</p>
      <ul>
        <li>
          Certificado literal de nacimiento del interesado inscrito en el Registro Civil español.
        </li>
      </ul>

      <br>

      <p class="fw-bold">Las personas que no hayan ejercido oportunamente la facultad de optar:</p>
      <ul>
        <li>
          Certificación de nacimiento del padre/madre español/a.
        </li>
        <li>
          Certificación literal de nacimiento del interesado inscrito en un Registro Civil español o resolución judicial
          donde conste la adopción por español o el reconocimiento de la filiación.
        </li>
      </ul>

      <br>

      <p class="fw-bold">Personas en régimen de tutela, guarda o acogimiento no provisional:</p>
      <ul>
        <li>
          En casos de acogimiento familiar: Autorización del Tribunal competente por el que se designe a la persona que
          va
          a ejercer la tutela, guarda o acogimiento.
        </li>
        <li>
          En los casos de acogimiento por Institución española: Resolución de dicha Institución asumiendo la tutela,
          guarda o acogimiento.
        </li>
      </ul>

      <br>

      <p class="fw-bold">Casado o casada con español/a:</p>
      <ul>
        <li>
          Certificación literal de nacimiento del cónyuge español, expedido por el Registro civil español.
        </li>
        <li>
          Certificación literal de matrimonio expedido por el Registro civil español.
        </li>
        <li>
          Certificado de Convivencia o Empadronamiento conjunto con el cónyuge.
        </li>
      </ul>

      <br>

      <p class="fw-bold">Viudo o viuda de español/a:</p>
      <ul>
        <li>
          Certificación literal de nacimiento del cónyuge expedido por Registro civil español.
        </li>
        <li>
          Certificación literal de matrimonio expedido por Registro civil español actualizado, esto es, expedido en
          fechas próximas a la solicitud de nacionalidad.
        </li>
        <li>
          Certificación de defunción del cónyuge.
        </li>
        <li>
          Certificado de empadronamiento conjunto o convivencia a la fecha de fallecimiento del cónyuge.
        </li>
      </ul>

      <br>

      <p class="fw-bold">Descendiente de español:</p>
      <ul>
        <li>
          Certificación literal de nacimiento del padre/madre español/a.
        </li>
        <li>
          Certificación literal de nacimiento abuelo/abuela, sólo cuando uno de ellos, o ambos, sean ascendientes
          españoles. En este caso, deberá presentarse también la certificación de nacimiento del padre/madre
          descendiente de español, aunque él/ella haya sido o no español.
        </li>
      </ul>

      <br>

      <p class="fw-bold">Sefardies:</p>
      <ul>
        <li>
          Documentación acreditativa de la condición de sefardí conforme a lo establecido en el artículo 1.2 de la Ley
          12/2015, de 24 de junio, en materia de concesión de la nacionalidad española a los sefardíes originarios de
          España.
        </li>
      </ul>
    </div>
  </div>

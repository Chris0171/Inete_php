      <h1 class="text-center">Actualización</h1>
      <br>
      <p>
        Como es conocido está aprobado un nuevo Reglamento o una reforma de la ley de Extranjería donde la normativa se
        ajusta más a la situación migratoria actual, a las necesidades del mercado laboral español y de las personas
        migrantes. Esta nueva reforma se considera una de las más integrales realizadas en los últimos años en España.
        Entrará en vigor a partir del día 20 de mayo de 2025 <span class="fw-bold"><a
            href="https://www.boe.es/buscar/act.php?id=BOE-A-2024-24099" target="_blank">[Aquí]</a></span>.
      </p>

      <h3>Entre las principales novedades de la nueva ley o reforma de Ley, tenemos:</h3>
      <p class="fw-bold">En caso de los Visados</p>
      <p>
        Todas las autorizaciones iniciales van a ser de un año y las renovaciones, de cuatro años. Esto evita la
        situación irregular sobrevenida. También se simplificarán los trámites para el cambio de una situación de
        residencia a otra.
      </p>
      <p>
        Será extendido a un año el visado para la búsqueda de empleo, que antes esta autorización era de 3 meses. Esto
        va a facilitar que las personas consigan un trabajo adecuado a su perfil profesional y también que las empresas
        encuentren a los profesionales que necesitan.
      </p>

      <br>

      <p class="fw-bold">En caso de la Formación</p>
      <p>
        Los estudiantes podrán tener una autorización de estancia por estudios que dure el mismo tiempo que su
        formación, y una vez culminada su formación se les facilitará una vía rápida para poder vincular con una
        autorización de trabajo.
      </p>
      <p>
        Con independencia de la formación que estarán cursando, estarán autorizados a trabajar un máximo de 30 horas
        semanales.
      </p>

      <br>

      <p class="fw-bold">En caso de Trabajo</p>
      <p>
        En este caso, la mayoría de las autorizaciones reguladas en el reglamento autorizan a trabajar desde el
        principio sin tener que solicitar una autorización inicial de trabajo por cuenta ajena. Esto incluye a las
        personas que van a realizar estudios en España, que podrán trabajar hasta 30 horas a la semana.
      </p>
      <p>
        Para las actividades de temporada, se creó una autorización de residencia y trabajo que facilita la contratación
        tanto a nivel individual como colectivo. También se protegen mejor los derechos de los trabajadores porque
        tendrán información escrita (en un idioma que puedan comprender) de sus condiciones laborales, de su estancia y
        gastos, entre otras.
      </p>

      <br>

      <p class="fw-bold">En caso de arraigos</p>
      <p>
        En esta nueva reforma habrá cinco modalidades de arraigo para regularizar a las personas en España: el arraigo
        social, Arraigo socio-laboral, arraigo familiar, arraigo socio-formativo y el arraigo de segunda oportunidad
        (este va dirigido a personas que hayan tenido una autorización de residencia en los últimos dos años y no la
        hubiesen renovado por diferentes razones). Todas estas autorizaciones serán de un año excepto la de arraigo
        familiar que será de cinco años.
      </p>

      <br>

      <p class="fw-bold">En caso de Reagrupación Familiar</p>
      <p>
        Se creó una autorización de residencia para familiares de ciudadanos españoles, por ejemplo, incluir a las
        parejas no registradas formalmente que puedan demostrar su relación.
      </p>
      <p>
        En el caso de los hijos de ciudadanos españoles se amplía la edad hasta los 26 años, cuando antes era de 21,
        además de contemplarse otras situaciones.
      </p>
      <p>
        La mayoría de las personas beneficiadas con estas medidas, son las que han obtenido la nacionalidad española en
        estos últimos años ya que podrán llevarse su familia a España.
      </p>


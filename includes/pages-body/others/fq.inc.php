      <h2>Sobre nacionalidad por residencia</h2>

      <div class="accordion" id="accordionFlushExample2">
        <!--  ¿Cuáles son los requisitos para los ciudadanos de países iberoamericanos? -->
        <div class="accordion-item accordion-st">
          <h2 class="accordion-header">
            <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse"
              data-bs-target="#flush-collapse2_1" aria-expanded="false" aria-controls="flush-collapse2_1">
              ¿Cuáles son los requisitos para los ciudadanos de países iberoamericanos?
            </button>
          </h2>
          <div id="flush-collapse2_1" class="accordion-collapse collapse" data-bs-parent="#accordionFlushExample2">
            <div class="accordion-body">
              <ul>
                <li>
                  Residir en España de forma legal: Se debe haber residido en un periodo de tiempo de 2 años en España
                  de forma legal y continuada, inmediatamente antes de la fecha en la que solicites tu nacionalidad.
                </li>
                <li>
                  Se debe demostrar una buena conducta cívica en España.
                  <ul>
                    <li>
                      Demostrando que se carece de antecedentes penales o policiales en España y en su país de origen.
                    </li>
                    <li>
                      Demostrar conocimiento de la cultura española y la integración en España. Esto se demuestra con el
                      examen CCEE de conocimientos generales de la cultura española del Instituto de Cervantes.
                    </li>
                    <li>
                      Si en su país de origen el español no es un idioma oficial, debe demostrar además que tiene un
                      nivel de español básico a través de la prueba DELE A2, que se imparte por el Instituto cervantes.
                    </li>
                  </ul>
                </li>
              </ul>
            </div>
          </div>
        </div>

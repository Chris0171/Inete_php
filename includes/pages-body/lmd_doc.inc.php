    <!-- Documentación a aportar el día de la cita para entrega del expediente -->
    <h3 class="text-underline">Documentación a aportar el día de la cita para entrega del expediente</h3>
    <p>
      Los documentos a aportar se relacionan a continuación según el caso o anexo a presentar. El registro civil o la
      oficina consular podrá requerir o solicitar documentos adicionales si son necesarios para la valoración del
      expediente.
    </p>
    <p>
      Las certificaciones registrales extranjeras a presentar, deben entregarse debidamente legalizadas o apostilladas,
      según sea el caso. En caso de documentos que no estén en idioma español deberán aportarse traducidas por el órgano
      o funcionario competente.
    </p>
    <!-- Documentación a aportar para el caso de Anexo I -->
    <h3 class="mt-4">Documentación a aportar para el caso de Anexo I:</h3>
    <ul>
      <li>
        Solicitud (original y copia) debidamente completada y firmada. (anexo I).
      </li>
      <li>
        Hoja declaratoria de datos debidamente completada y firmada.
      </li>
      <li>
        Documento de identidad del solicitante.
      </li>
      <li>
        Certificación de nacimiento del solicitante, expedida por el Registro Civil local en que se encuentra inscrito.
      </li>
      <li>
        Certificación de nacimiento del Padre o Madre que originariamente hubiera sido español (en caso de presentar
        por hijo). En caso de no tener certificado de Nacimiento del registro civil, debe aportar el certificado de
        bautismo junto a la negativa de nacimiento del registro civil.
      </li>
      <li>
        En caso de presentarse por nieto de originario español, debe aportar la Certificación de nacimiento del padre
        o madre (línea española), y certificación de nacimiento del abuelo o abuela. En caso de no tener certificado de
        Nacimiento del registro civil, debe aportar el certificado de bautismo junto a la negativa de nacimiento del
        registro civil.
      </li>
      <li>
        Documentación que acredite la condición de exiliado del padre, madre, abuelo o abuela según el caso.
      </li>
    </ul>
    <p>
      Además de los documentos que se relacionaron a continuación para cada uno de los supuestos, la Oficina Consular
      podrá pedir datos o documentos adicionales cuando sean necesarios para valorar el expediente.
    </p>
    <!-- Documentación a aportar para el caso de Anexo II -->
    <h3 class="mt-4">Documentación a aportar para el caso de Anexo II:</h3>
    <ul>
      <li>
        Solicitud (original y copia) debidamente completada y firmada. (anexo II).
      </li>
      <li>
        Hoja declaratoria de datos debidamente completada y firmada.
      </li>
      <li>
        Documento de identidad del solicitante.
      </li>
      <li>
        Certificación de nacimiento del solicitante, expedida por el Registro Civil local en que se encuentra
        inscrito.
      </li>
      <li>
        Certificación literal de nacimiento de la madre española del solicitante.
      </li>
      <li>
        Certificado literal de matrimonio de la madre española con el extranjero, contraído antes del 29 de diciembre de
        1978, expedida por el Registro Civil donde se encuentre inscrito.
      </li>
      <li>
        Para matrimonios formalizados entre el 5 de agosto de 1954 y el 28 de diciembre de 1978, aportar documentación
        que acredite la adquisición por la madre de la nacionalidad del marido y documento acreditativo de la
        legislación extranjera en materia de adquisición de la nacionalidad por matrimonio vigente en la fecha en que
        éste se formalizó.
      </li>
    </ul>
    <!-- Documentación a aportar para el caso de Anexo III -->
    <h3 class="mt-4">Documentación a aportar para el caso de Anexo III</h3>
    <ul>
      <li>
        Solicitud (original y copia) debidamente completada y firmada. (anexo III).
      </li>
      <li>
        Hoja declaratoria de datos debidamente completada y firmada.
      </li>
      <li>
        Documento de identidad del solicitante.
      </li>
      <li>
        Certificación de nacimiento del solicitante, expedida por el Registro Civil local en que se encuentra
        inscrito.
      </li>
      <li>
        Certificación literal de nacimiento española del padre o de la madre del solicitante mayor de edad, al haberse
        reconocido a su progenitor la nacionalidad española de origen de acuerdo a la Ley de Memoria Democrática de 2022
        o la Ley de Memoria Histórica de 2007, cuando la solicitud sea presentada en un Registro Civil distinto a aquel
        en el que se encuentra inscrito el nacimiento del padre o de la madre.
      </li>
    </ul>

    <h2>INETE: Información de nacionalidad española y tramites de Extranjería.</h2>
    <!-- ¿Quiénes pueden optar? -->
    <p>La obtención de la nacionalidad española permite a la persona no solo vivir en territorio español, sino que
      podrán viajar por todos los países del espacio Schengen sin control fronterizo, como son: Dinamarca, Eslovenia,
      España, Estonia, Finlandia, Francia, Grecia, Hungría, Islandia, Italia, Letonia, Liechtenstein, Lituania,
      Luxemburgo, Malta, Noruega, Países Bajos, Polonia, Portugal, República Checa, República Eslovaca, Suecia, Suiza,
      Alemania, Austria y Bélgica. También, la nacionalidad española te permite acceder al mercado laboral en todos
      estos países.
    </p>
    <br>
    <p class="fw-bold"><span class="text-danger">AVISO: </span>La información que se proporciona es solamente
      orientativa y se debe
      buscar asesoramiento con un experto o consultar la embajada o consulado de España más cercano a su localidad en su
      país de origen.</p>
    <h2>¿Quiénes pueden optar?</h2>
    <p>Según la ley de memoria democrática en la disposición adicional octava y la instrucción del 25 de octubre de
      2022, pueden optar:</p>
    <ul>
      <li>
        Los nacidos fuera de España de padre o madre, abuelo o abuela, que originariamente hubieran sido españoles.
      </li>
      <li>
        Los nacidos fuera de España de padre o madre, abuelo o abuela, que originariamente hubieran sido españoles. y
        que, como consecuencia de haber sufrido exilio por razones políticas, ideológicas o de creencia o de orientación
        e identidad sexual, hubieran perdido o renunciado a la nacionalidad española. Presentarían con Anexo I.
      </li>
      <li>
        Los hijos e hijas nacidos en el exterior de mujeres españolas que perdieron su nacionalidad por casarse con
        extranjeros antes de la entrada en vigor de la Constitución de 1978. Presentarían con Anexo II.
      </li>
      <li>
        Los hijos e hijas mayores de edad de aquellos españoles a quienes les fue reconocida su nacionalidad de origen
        en virtud del derecho de opción de acuerdo a lo dispuesto en la presente ley o en la disposición adicional
        séptima de la Ley 52/2007, de 26 de diciembre. Presentarían con Anexo III.
      </li>
    </ul>
    <p>- En todos los supuestos, esta declaración deberá formalizarse hasta el mes de octubre de 2025.</p>

